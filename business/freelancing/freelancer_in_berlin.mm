<node CREATED="1516461923667" ID="ID_825041121" LINK="http://lifeinduesseldorf.com/starting-business-germany-tax-guide-freelance-expats/" MODIFIED="1613222962247" TEXT="Germany is 19%">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Imagine you designed a brochure for Sabine and charged her &#8364;150. You would invoice Sabine &#8364;150 with an additional 19% VAT. That comes to &#8364;178.50 total.
    </p>
    <p>
      
    </p>
    <p>
      Sabine pays you &#8364;178,50. You, as a freelancer, will then tell the <i>Finanzamt</i>&#160;that you received &#8364;178.50 and will pay them &#8364;28,50 of that amount. At the end of each month, any VAT you collected with be transferred from your bank account to the German state.
    </p>
    <p>
      
    </p>
    <p>
      Because Sabine is also a businessperson, she will then claim that &#8364;28.50 back from the <i>Finanzamt</i>.
    </p>
    <p>
      Although, if she <i>weren&#8217;t</i>&#160;a businesswoman and instead just wanted a brochure designed for personal reasons, she could not claim back that &#8364;28.50. Either way though,&#160;<i>you</i>&#160;have to invoice her for it.<br /><br />
    </p>
    <p>
      <br />
      It&#8217;s a weird roundabout money flow. At the very least, try to understand the general system and make sure you collect that 19% from any client you invoice.
    </p>
    <p>
      
    </p>
    <p>
      The basic idea is that the VAT is paid not by business people, but by private people.
    </p>
    <p>
      If you invoice clients outside of Germany, but still in the EU (i.e. The Netherlands, France), you generally don&#8217;t need to include VAT in your invoices. However, if you invoice clients in the United States, you don&#8217;t need to charge VAT at all.
    </p>
    <p>
      &#160;Instead of VAT there are should be some additional information on the invoice. But if you work with German clients you have to include that 7% or 19% on all invoices.
    </p>
    <p>
      
    </p>
    <p>
      Each month you will transfer any VAT to the <i>Finanzamt</i>. You have to do this online. All VAT has to be reported 10 days after the month ends. So by the 10th of September you have to report your VAT and pay online for the month of August. The Finanzamt will just take that money from your bank account.
    </p>
    <p>
      
